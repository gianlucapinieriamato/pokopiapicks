// 6. Generate locations.ts
// ---------------------------------------------------------------------------

function genLocationsTs(): string {
  let out = banner;
  out += `import { Item, type ItemConst } from "./items";\n\n`;

  out += `export type ShopItemConst = {\n`;
  out += `  readonly item: ItemConst;\n`;
  out += `  readonly level: number;\n`;
  out += `};\n\n`;

  out += `export const Location = {\n`;
  let unresolved = 0;
  for (const e of locEntries) {
    const loc = LOCATIONS[e.slug];
    const desc = (loc?.description ?? "").replace(/\\/g, "\\\\").replace(/"/g, '\\"');
    const obj = (loc?.objective ?? "").replace(/\\/g, "\\\\").replace(/"/g, '\\"');

    const resolveItems = (slugs: readonly string[], context: string): string[] => {
      const resolved: string[] = [];
      for (const s of slugs) {
        const ik = itemKeyMap.get(s);
        if (!ik) {
          // Only warn for non-known unresolved slugs
          console.warn(`[locations:${context}] Unresolved item slug: ${s}`);
          unresolved++;
          continue;
        }
        resolved.push(`Item.${ik}`);
      }
      return resolved;
    };

    out += `  ${e.key}: {\n`;
    out += `    slug: "${e.slug}",\n`;
    out += `    label: "${(loc?.name ?? e.slug).replace(/"/g, '\\"')}",\n`;
    out += `    description: "${desc}",\n`;
    out += `    objective: "${obj}",\n`;

    const materials = resolveItems(loc?.materials ?? [], `${e.slug}/materials`);
    out += `    materials: [${materials.length ? "\n      " + materials.join(",\n      ") + ",\n    " : ""}] as ItemConst[],\n`;

    const blocksAndPlants = resolveItems(loc?.blocksAndPlants ?? [], `${e.slug}/blocksAndPlants`);
    out += `    blocksAndPlants: [${blocksAndPlants.length ? "\n      " + blocksAndPlants.join(",\n      ") + ",\n    " : ""}] as ItemConst[],\n`;

    const itemsInArea = resolveItems(loc?.itemsInArea ?? [], `${e.slug}/itemsInArea`);
    out += `    itemsInArea: [${itemsInArea.length ? "\n      " + itemsInArea.join(",\n      ") + ",\n    " : ""}] as ItemConst[],\n`;

    const itemsInPokeballs = resolveItems(loc?.itemsInPokeballs ?? [], `${e.slug}/itemsInPokeballs`);
    out += `    itemsInPokeballs: [${itemsInPokeballs.length ? "\n      " + itemsInPokeballs.join(",\n      ") + ",\n    " : ""}] as ItemConst[],\n`;

    const treasure = resolveItems(loc?.treasure ?? [], `${e.slug}/treasure`);
    out += `    treasure: [${treasure.length ? "\n      " + treasure.join(",\n      ") + ",\n    " : ""}] as ItemConst[],\n`;

    out += `    shopItems: [\n`;
    for (const si of (loc?.shopItems ?? [])) {
      const ik = itemKeyMap.get(si.slug);
      if (!ik) {
        console.warn(`[locations:${e.slug}/shopItems] Unresolved item slug: ${si.slug}`);
        unresolved++;
        continue;
      }
      out += `      { item: Item.${ik}, level: ${si.level} },\n`;
    }
    out += `    ] as ShopItemConst[],\n`;
    out += `  },\n`;
  }
  out += `} as const;\nexport type LocationConst = typeof Location[keyof typeof Location];\nexport type LocationKey = keyof typeof Location;\n`;
  if (unresolved > 0) console.warn(`[locations] Total unresolved items: ${unresolved}`);
  return out;
}

