function genHabitatConfigTs(): string {
  let out = banner;
  out += `import { Item, type ItemConst } from "./items";\n\n`;

  out += `export type HabitatRequirementItem = {\n`;
  out += `  type: "item";\n`;
  out += `  item: ItemConst;\n`;
  out += `  label: string;\n`;
  out += `  qty: number;\n`;
  out += `};\n\n`;
  out += `export type HabitatRequirementGroup = {\n`;
  out += `  type: "group";\n`;
  out += `  groupKey: string;\n`;
  out += `  label: string;\n`;
  out += `  qty: number;\n`;
  out += `};\n\n`;
  out += `export type HabitatRequirementConst = HabitatRequirementItem | HabitatRequirementGroup;\n\n`;

  out += `export const HabitatConfig = {\n`;
  let unresolved = 0;
  for (const e of habEntries) {
    const hab = HABITATS[e.slug];
    const desc = (hab?.description ?? "").replace(/\\/g, "\\\\").replace(/"/g, '\\"');
    out += `  ${e.key}: {\n`;
    out += `    slug: "${e.slug}",\n`;
    out += `    label: "${(hab?.name ?? e.slug).replace(/"/g, '\\"')}",\n`;
    out += `    description: "${desc}",\n`;
    out += `    requirements: [\n`;
    const reqs = HABITAT_REQUIREMENTS[e.slug] ?? [];
    for (const req of reqs) {
      const label = req.name.replace(/\\/g, "\\\\").replace(/"/g, '\\"');
      if (req.type === "item") {
        const ik = itemKeyMap.get(req.value);
        if (!ik) {
          console.warn(`[habitat-config] Unresolved item slug: ${req.value} in ${e.slug}`);
          unresolved++;
          continue;
        }
        out += `      { type: "item" as const, item: Item.${ik}, label: "${label}", qty: ${req.qty} },\n`;
      } else {
        // group type: req.value is the group key
        out += `      { type: "group" as const, groupKey: "${req.value}", label: "${label}", qty: ${req.qty} },\n`;
      }
    }
    out += `    ] as HabitatRequirementConst[],\n`;
    out += `  },\n`;
  }
  out += `} as const;\nexport type HabitatConfigConst = typeof HabitatConfig[keyof typeof HabitatConfig];\nexport type HabitatConfigKey = keyof typeof HabitatConfig;\n`;
  if (unresolved > 0) console.warn(`[habitat-config] Total unresolved items: ${unresolved}`);
  return out;
}

